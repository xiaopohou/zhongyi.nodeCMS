  adminMain.controller('roleController',function($scope,$rootScope,$timeout,$dialogs){
   
  $scope.name = '';
  
  $scope.openWin = function(which){
    var dlg = null;
    switch(which){

      // Create Your Own Dialog
      case 'create':
        dlg = $dialogs.create('/dialogs/whatsyourname.html',
        'whatsYourNameCtrl',
        {},
        {key: false,back: 'static'}
        );
        
        dlg.result.then(function(name){
          $scope.name = name;
        },function(){
          $scope.name = 'You decided not to enter in your name, that makes me sad.';
        });
        break;
    }; // end switch
  }; // end launch
  
 
  
})




 
.controller('whatsYourNameCtrl',function($scope,$modalInstance,data){
  $scope.user = {name : ''};

  $scope.cancel = function(){
    $modalInstance.dismiss('canceled');  
  }; // end cancel
  
  $scope.save = function(){
    $modalInstance.close($scope.user.name);
  }; // end save
  
  $scope.hitEnter = function(evt){
    if(angular.equals(evt.keyCode,13) && !(angular.equals($scope.name,null) || angular.equals($scope.name,'')))
				$scope.save();
  }; // end hitEnter
}) // end whatsYourNameCtrl
